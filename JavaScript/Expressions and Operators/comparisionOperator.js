// Comparision Operators

// Will compare values and return a Boolean as a respond to the comparison

let one = 1
let two = 2


// == same a
console.log(two == 1)
console.log(one == "1")

// != diferent 

console.log(one != two)
console.log(one != 1)
console.log(one != "1")

// === strict equality (same value and type)
console.log(one === 1)
console.log(one === "1")

// !== strict diferent (diferent value and type)
console.log(one !== 1)
console.log(one !== "1")

//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-

// > biggest than...
console.log(one > two)

// >= biggest equal to...
console.log(one >= two)

// < smallest than...
console.log(one < two)

// <= smallest equal to...
console.log(one <= two)