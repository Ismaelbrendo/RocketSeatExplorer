// Condition Operator (ternary)


// Depending on the condition, we receive different values 

// Condition then value 1 if not value 2
// Condition ? value1 : value2

// Exemples

// Breakfast top
let bread = true
let cheese = true
let coofee = false
const niceBreakfast = bread && cheese && coofee ? 'CAFEZAO TOP' : 'COOFEE BAD OF PEST'

console.log(niceBreakfast)


